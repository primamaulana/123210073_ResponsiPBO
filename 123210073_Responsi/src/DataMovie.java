    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if (i == -1) {
            return;
        }

        String judul = (String) model.getValueAt(i, 0);

        int pernyataan = JOptionPane.showConfirmDialog(null, "Yakin data akan dihapus?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (pernyataan == JOptionPane.OK_OPTION) {
            try {
                String sql = "DELETE FROM movie where judul=?";
                conn = new Konektor();
                koneksi = conn.getKoneksi();
                pst = koneksi.prepareStatement(sql);
                pst.setString(1, judul);
                pst.executeUpdate();
                pst.close();
                JOptionPane.showMessageDialog(null, "Data terhapus");
            } catch (Exception e) {
                System.out.println("Delete Error");
                System.out.println(e);
            } finally {
                loadData();
                clear();
                txJudul.setEnabled(true);
            }
        }
        else if (pernyataan == JOptionPane.CANCEL_OPTION) {
            
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

