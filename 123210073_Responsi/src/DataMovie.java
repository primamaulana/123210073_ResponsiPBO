    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String judul = txJudul.getText();
        String alur = txAlur.getText();
        String penokohan = txPenokohan.getText();
        String akting = txAkting.getText();

        double nilai = 0;
        double nalur, ntokoh, nakting;
        nalur = Double.valueOf(alur);
        ntokoh = Double.valueOf(penokohan);
        nakting = Double.valueOf(akting);
        nilai = (nalur + ntokoh + nakting) / 3;

        try {
            String sql = "INSERT INTO movie VALUES(?,?,?,?,?)";
            conn = new Konektor();
            koneksi = conn.getKoneksi();
            pst = koneksi.prepareStatement(sql);

            pst.setString(1, judul);
            pst.setString(2, alur);
            pst.setString(3, penokohan);
            pst.setString(4, akting);
            pst.setDouble(5, nilai);
            pst.executeUpdate();
            pst.close();
        } catch (Exception e) {
            System.out.println(e);
        }
        loadData();
        clear();
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if (i == -1) {
            return;
        }
        String judul = txJudul.getText();
        String alur = txAlur.getText();
        String penokohan = txPenokohan.getText();
        String akting = txAkting.getText();

        double nilai = 0;
        double nalur, ntokoh, nakting;
        nalur = Double.valueOf(alur);
        ntokoh = Double.valueOf(penokohan);
        nakting = Double.valueOf(akting);
        nilai = (nalur + ntokoh + nakting) / 3;

        try {
            String sql = "UPDATE movie set alur=?, penokohan=?, akting=?, nilai=? WHERE judul=?";
            conn = new Konektor();
            koneksi = conn.getKoneksi();
            pst = koneksi.prepareStatement(sql);

            pst.setString(1, alur);
            pst.setString(2, penokohan);
            pst.setString(3, akting);
            pst.setDouble(4, nilai);
            pst.setString(5, judul);

            pst.executeUpdate();
            pst.close();
            JOptionPane.showMessageDialog(null, "Data berhasil dirubah");
        } catch (Exception e) {
            System.out.println("Update error");
            System.out.println(e);
        } finally {
            loadData();
            clear();
            txJudul.setEnabled(true);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

